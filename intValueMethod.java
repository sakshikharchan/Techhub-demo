public class intValueMethod
{
	public static void main(String x[])
	{
	
	
	Float f =5.4f;
	Double d =2.3;
	Long l=200L;
	
	int p=f.intValue();
	int q=d.intValue();
	int r=l.intValue();
	
	System.out.println("P is ="+p);
	System.out.println("Q is ="+q);
	System.out.println("R is ="+r);
	
	}
}